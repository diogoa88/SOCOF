-export([factorial/1, fib/1]).



%% ====================================================================
%% Internal functions
%% ====================================================================


factorial(1) -> 1;
factorial(N) -> N * factorial(N-1).

fib(0)->0;
fib(1)->1;
fib(N)-> fib(N-1) + fib(N-2).